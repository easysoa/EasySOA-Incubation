package org.easysoa.registry.rest.jaxb;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlRootElement;

import org.codehaus.jackson.annotate.JsonTypeName;


/**
 * Wrapper for int (or Integer) property
 * 
 * @author mdutoo
 *
 */
@JsonTypeName("integer")
@XmlRootElement(name="integer")
@XmlAccessorType(XmlAccessType.FIELD)
public class SoaNodeIntegerProperty extends SoaNodeProperty {

    @XmlAttribute
    private int value;

    public SoaNodeIntegerProperty() {
    }

    public SoaNodeIntegerProperty(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
